public class RelacionCustomer {
    
    String idRelacionRC, idRelacionSR, idCustomerContact, NombreContacto, EmailContacto;

    public String getIdRelacionRC() {
        return idRelacionRC;
    }

    public void setIdRelacionRC(String idRelacionRC) {
        this.idRelacionRC = idRelacionRC;
    }

    public String getIdRelacionSR() {
        return idRelacionSR;
    }

    public void setIdRelacionSR(String idRelacionSR) {
        this.idRelacionSR = idRelacionSR;
    }

    public String getIdCustomerContact() {
        return idCustomerContact;
    }

    public void setIdCustomerContact(String idCustomerContact) {
        this.idCustomerContact = idCustomerContact;
    }

    public String getNombreContacto() {
        return NombreContacto;
    }

    public void setNombreContacto(String NombreContacto) {
        this.NombreContacto = NombreContacto;
    }

    public String getEmailContacto() {
        return EmailContacto;
    }

    public void setEmailContacto(String EmailContacto) {
        this.EmailContacto = EmailContacto;
    }
    
    
    
}
